#define DATA_SIZE 100

int result = 1; // 存放结果

// =========================================================
// 主函数
// =========================================================
int main() {
    int i;

    // 向量乘法
    for (i = 1; i <= DATA_SIZE; i++) {
        result = result * i;
    }

    // 3. 结束
    return result;
}